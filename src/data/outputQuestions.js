// Helper function to get questions based on priority scores
export function getPersonalizedQuestions(priorityScores) {
  const selectedQuestions = [];

  Object.keys(priorityScores).forEach(dimension => {
    const score = priorityScores[dimension];
    const dimensionQuestions = outputQuestions[dimension];

    if (score === 'high') {
      // Show 5-6 questions for high priority
      selectedQuestions.push(
        ...dimensionQuestions
          .filter(q => q.priority === 'essential' || q.priority === 'high')
          .slice(0, 6)
      );
    } else if (score === 'medium') {
      // Show 3-4 questions for medium priority
      selectedQuestions.push(
        ...dimensionQuestions
          .filter(q => q.priority === 'essential' || q.priority === 'high')
          .slice(0, 4)
      );
    } else {
      // Show 2-3 essential questions for low priority
      selectedQuestions.push(
        ...dimensionQuestions
          .filter(q => q.priority === 'essential')
          .slice(0, 3)
      );
    }
  });

  return selectedQuestions;
}
